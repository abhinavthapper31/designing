package lowLevelDesign.cacheLLD.Algo;

public class Node<Key> {

    private Key key;

    public Node(Key key) {
        this.key = key;
    }

    public Key getKey() {
        return key;
    }

    public void setKey(Key key) {
        this.key = key;
    }

    @Override
    public String toString() {
        return "Node{" +
                "key=" + key +
                '}';
    }
}
